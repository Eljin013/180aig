package a1;

public class MoveScore {
	private String move;
	private int score;
	
	public MoveScore() {
		
	}

	public String getMove() {
		return move;
	}

	public void setMove(String move) {
		this.move = move;
	}

	public int getScore() {
		return score;
	}

	public void setScore(int score) {
		this.score = score;
	}
	
}
